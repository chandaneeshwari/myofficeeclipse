package com.virtusa.lp.model;
public class Assessment 
{
	public int assesmentId;
	public String password;
	public String questionPaper;
	public int getAssesmentId() 
	{
		return assesmentId;
	}
	public void setAssesmentId(int assesmentId1) 
	{
		assesmentId = assesmentId1;
	}
	public String getPassword()
	{
		return password;
	}
	public void setPassword(String password1)
	{
		password = password1;
	}
	public String getQuestionPaper() 
	{
		return questionPaper;
	}
	public void setQuestionPaper(String questionPaper1) 
	{
		questionPaper = questionPaper1;
	}
}